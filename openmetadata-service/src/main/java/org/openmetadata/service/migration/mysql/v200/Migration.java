package org.openmetadata.service.migration.mysql.v200;

import lombok.SneakyThrows;
import lombok.extern.slf4j.Slf4j;
import org.openmetadata.service.migration.api.MigrationProcessImpl;
import org.openmetadata.service.migration.utils.MigrationFile;
import org.openmetadata.service.migration.utils.v200.MigrationUtil;

@Slf4j
public class Migration extends MigrationProcessImpl {

  public Migration(MigrationFile migrationFile) {
    super(migrationFile);
  }

  @Override
  @SneakyThrows
  public void runDataMigration() {
    LOG.info("Running OpenMetadata 2.0.0 data migration - Task System Redesign");

    // Migrate Thread-based tasks to new Task entity system
    MigrationUtil.migrateThreadTasksToTaskEntity(handle, false);

    // Update the task sequence to continue from the highest migrated task ID
    MigrationUtil.updateTaskSequence(handle, false);

    LOG.info("OpenMetadata 2.0.0 data migration completed");
  }
}
